package tek.week_5.day_3;

public class WhileLoopActivity {

    // Print the values starting from 1 to 10;
    // USE THE WHILE LOOP IN ORDER TO PRINT THE VALUES FROM 1 - 10 !

    public static void main(String[] args) {
       int number = 1;

       while ( number <= 10 ) {
           System.out.println(number);
           number++;
       }
    }
}
